package gr.aueb.cf.ch3;

/**
 *Prints 10 stars in horizontal axis,
 * 1 star each time for 10 loops.
 *
 *
 */


public class Stars10App {
    public static void main(String[] args) {

        int i = 1;

        while (i <= 10) {
            System.out.println("*");
            i++;
        }
    }
}
